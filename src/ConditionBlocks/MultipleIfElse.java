package ConditionBlocks;

public class MultipleIfElse {

	public static void main(String[] args) {
		
		int age =70;
		
		if (age<20) {
			System.out.println("hello");
		}
		
		else if (age==70) {
			
			System.out.println("hello i am inside elseif");
			
		}
		
		else {
			
			System.out.println("heii");
		}
		
		
		
	}
	
}
