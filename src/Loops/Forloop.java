package Loops;

public class Forloop {

	public static void main(String[] args) {
		
		for (int i=1;i<=100;i++) {
			
			if (i%2==0) {
				System.out.println("even number is"+" "+i);
			}
			
			
			
			else {
				
				System.out.println("odd number is"+" "+i);
			}
			
		//	System.out.println(i);
		}
		
	}
	
	
}
